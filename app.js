let show=document.querySelector("#show");

function sinf(){
    show.value=Math.sin(show.value);
}

function cosf(){
    show.value=Math.cos(show.value);
}

function tanf(){
    show.value=Math.tan(show.value);
}

function sqrf(){
    show.value=Math.sqrt(show.value);
}
function powf(){
    show.value=Math.pow(show.value,2);
}